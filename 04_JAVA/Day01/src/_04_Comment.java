public class _04_Comment {
    public static void main(String[] args) {
        System.out.println("(5분) 일찍 오기 ");
        // 하지만 비가 와서 쉽지 않아요.
        // 그래도 일찍 와야겠죠?

        int size = 120;
        size = size + 10;
        System.out.println("사이즈는 " + (size - 10) + "입니다."); // 사이즈는 130입니다.

        int a = 10;
        int b = 20;
        int c = 30;
        int d = 40;
        System.out.println(a + b + c + d);

        // 한 줄 주석 단축키 : Ctrl + /
        // 여러줄 주석 단축키 : Ctrl + Shift + /
    }
}
